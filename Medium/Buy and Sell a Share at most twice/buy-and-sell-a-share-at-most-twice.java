class Solution {
    public static int maxProfit(int n, int[] prices) {
        if (n <= 1)
            return 0;
        
        int[] leftProfit = new int[n]; // max profit if sold on or before i
        int[] rightProfit = new int[n]; // max profit if bought on or after i
        
        // Forward pass: calculate max profit from left to right
        int minPrice = prices[0];
        for (int i = 1; i < n; i++) {
            minPrice = Math.min(minPrice, prices[i]);
            leftProfit[i] = Math.max(leftProfit[i - 1], prices[i] - minPrice);
        }
        
        // Backward pass: calculate max profit from right to left
        int maxPrice = prices[n - 1];
        for (int i = n - 2; i >= 0; i--) {
            maxPrice = Math.max(maxPrice, prices[i]);
            rightProfit[i] = Math.max(rightProfit[i + 1], maxPrice - prices[i]);
        }
        
        int maxProfit = 0;
        // Find the maximum combined profit
        for (int i = 0; i < n; i++) {
            maxProfit = Math.max(maxProfit, leftProfit[i] + (i + 1 < n ? rightProfit[i + 1] : 0));
        }
        
        return maxProfit;
    }
}
